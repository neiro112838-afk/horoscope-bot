async def scheduler():
    """–ü–ª–∞–Ω–∏—Ä–æ–≤—â–∏–∫: –∂–¥—ë–º 8:00 –ú–°–ö –∏ –∑–∞–ø—É—Å–∫–∞–µ–º send_daily()."""
    while True:
        now = datetime.now()
        next_8 = now.replace(hour=8, minute=0, second=0, microsecond=0)
        if now >= next_8:
            next_8 += timedelta(days=1)
        sleep_seconds = (next_8 - now).total_seconds()
        await asyncio.sleep(sleep_seconds)
        await send_daily()

